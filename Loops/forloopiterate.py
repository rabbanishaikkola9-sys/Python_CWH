
##For loop with lists
li=[1,2,3,4,5]
for i in li:
    print(i)
##For loop with tuples

tuple=(1,2,3,True)
for i in tuple:
    print(i)
##For loop with strings
name="rabbani"
for i in name:
    print(i)
##For loops with dictionaries
dict={
    "rabbani":100,
    "rehaan":99,
    "key":"value"
}
